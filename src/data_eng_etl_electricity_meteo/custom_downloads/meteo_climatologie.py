def _stream_to_file(url: str, *, client: httpx.Client, path: Path) -> None:
    """Stream-download a URL to a local file.

    Parameters
    ----------
    url
        URL to download.
    client
        HTTP client.
    path
        Local file path to write.
    """
    with client.stream("GET", url) as response:
        response.raise_for_status()
        with path.open("wb") as f:
            for chunk in response.iter_bytes():
                f.write(chunk)


def _download_department(
    resource: DepartmentResource,
    *,
    client: httpx.Client,
    tmp_dir: Path,
) -> int:
    """Download a single department, preferring Parquet to CSV.gz.

    The downloaded data is normalized to all-Utf8 columns and written as a
    per-department Parquet file in ``tmp_dir``.

    Parameters
    ----------
    resource
        Department download URLs.
    client
        HTTP client.
    tmp_dir
        Temporary directory for intermediate files.

    Returns
    -------
    int
        Number of rows downloaded (0 if both downloads fail).
    """
    dept = resource["dept"]
    parquet_url = resource["parquet_url"]
    csv_url = resource["csv_url"]

    df: pl.DataFrame | None = None

    # Try Parquet (Hydra) first — ~20x smaller than CSV.gz
    if parquet_url:
        tmp_path = tmp_dir / f"{dept}_hydra.parquet"
        try:
            _stream_to_file(parquet_url, client=client, path=tmp_path)
            # Column projection: 196 → 16 columns at read time
            df = pl.read_parquet(tmp_path, columns=_LANDING_COLUMNS)
            # Cast to Utf8 for uniform schema across departments
            df = df.cast({c: pl.Utf8 for c in df.columns})
            logger.debug("Downloaded Parquet (Hydra)", department=dept, rows_count=len(df))
        except (httpx.HTTPError, pl.exceptions.PolarsError, OSError) as err:
            logger.warning(
                "Parquet download failed, falling back to CSV.gz",
                department=dept,
                error=str(err),
                error_type=type(err).__qualname__,
            )
            df = None
        finally:
            tmp_path.unlink(missing_ok=True)

    # Fallback to CSV.gz
    if df is None:
        tmp_path = tmp_dir / f"{dept}.csv.gz"
        try:
            _stream_to_file(csv_url, client=client, path=tmp_path)
            # Column projection + infer_schema=False → 16 Utf8 columns
            df = pl.read_csv(tmp_path, separator=";", infer_schema=False, columns=_LANDING_COLUMNS)
            logger.debug("Downloaded CSV.gz", department=dept, rows_count=len(df))
        except (httpx.HTTPError, pl.exceptions.PolarsError, OSError) as err:
            logger.warning(
                "Failed to download department",
                department=dept,
                url=csv_url,
                error=str(err),
                error_type=type(err).__qualname__,
            )
            return 0
        finally:
            tmp_path.unlink(missing_ok=True)

    out_path = tmp_dir / f"{dept}.parquet"
    df.write_parquet(out_path)
    return len(df)


# --------------------------------------------------------------------------------------
# Parallel download orchestration
# --------------------------------------------------------------------------------------


@dataclass(frozen=True, slots=True, kw_only=True)
class _BatchResult:
    """Aggregated result of the parallel department download."""

    dept_count: int
    failed_count: int
    total_rows: int


def _download_all_departments(
    resources: dict[str, DepartmentResource],
    *,
    client: httpx.Client,
    tmp_dir: Path,
    progress: BatchProgressFactory | None = None,
) -> _BatchResult:
    """Download all departments in parallel with progress tracking.

    Parameters
    ----------
    resources
        Mapping ``dept → DepartmentResource`` with download URLs.
    client
        Thread-safe HTTP client with shared connection pool.
    tmp_dir
        Temporary directory for per-department Parquet files.
    progress
        Factory called with ``total_items`` that returns a progress reporter.
        Pass ``None`` (default) to use the built-in tqdm progress bar.

    Returns
    -------
    _BatchResult
        Aggregated counts of successful, failed, and total rows.
    """
    dept_count = 0
    failed_count = 0
    total_rows = 0

    with ThreadPoolExecutor(max_workers=_DOWNLOAD_MAX_WORKERS) as pool:
        futures = {
            pool.submit(_download_department, resource, client=client, tmp_dir=tmp_dir): resource
            for resource in resources.values()
        }

        reporter: DownloadProgressReporter = (
            progress(len(futures))
            if progress is not None
            else tqdm(
                total=len(futures),
                unit="dept",
                desc="Downloading departments",
                file=sys.stderr,
                leave=False,
            )
        )

        try:
            for future in as_completed(futures):
                resource = futures[future]
                try:
                    rows = future.result()
                except (pl.exceptions.PolarsError, OSError):
                    logger.exception(
                        "Department download failed unexpectedly",
                        department=resource["dept"],
                    )
                    rows = 0
                if rows > 0:
                    dept_count += 1
                    total_rows += rows
                else:
                    failed_count += 1
                reporter.update(1)
        finally:
            reporter.close()

    return _BatchResult(dept_count=dept_count, failed_count=failed_count, total_rows=total_rows)


# --------------------------------------------------------------------------------------
# Main entry point
# --------------------------------------------------------------------------------------


def download_climatologie(
    dest_dir: Path,
    progress: BatchProgressFactory | None = None,
    *,
    year_start: int | None = None,
    year_end: int | None = None,
) -> Path:
    """Download all departmental data and merge into a single Parquet.

    Starts from a base of hardcoded CSV.gz URLs for all 95 departments, then enriches
    with Parquet Hydra URLs discovered via the data.gouv.fr API.
    Each department is downloaded independently: Parquet if available, CSV.gz otherwise.
    If the API is unreachable, all departments use CSV.gz.

    Parameters
    ----------
    dest_dir
        Landing directory to write the merged parquet file.
    progress
        Factory called with ``total_items`` that returns a progress reporter.
        Pass ``None`` (default) to use the built-in tqdm progress bar.
    year_start
        Start year for the 2-year window. Defaults to ``current_year - 1``.
    year_end
        End year for the 2-year window. Defaults to ``current_year``.

    Returns
    -------
    Path
        Path to the merged parquet file.

    Raises
    ------
    DownloadStageError
        If no data could be downloaded from any department.
    """
    now = datetime.now(tz=UTC)
    if year_start is None:
        year_start = now.year - 1
    if year_end is None:
        year_end = now.year

    # -- Initialize directories and HTTP client ----------------------------------------

    dest_dir.mkdir(parents=True, exist_ok=True)

    tmp_dir = dest_dir / "_tmp_departments"
    tmp_dir.mkdir(parents=True, exist_ok=True)

    timeout = httpx.Timeout(timeout=settings.download_timeout_seconds)

    # -- Build resource list (fallback + API discovery) --------------------------------

    with httpx.Client(timeout=timeout, follow_redirects=True) as client:
        # Base: CSV.gz for all 95 departments (always works, no API needed)
        resources_by_dept = _build_fallback_resources(year_start=year_start, year_end=year_end)

        # Enrich with API discovery (adds Parquet URLs where available)
        try:
            api_resources = _fetch_department_resources(
                client=client, year_start=year_start, year_end=year_end
            )
            resources_by_dept.update(api_resources)
        except httpx.HTTPError as err:
            logger.warning("API discovery failed, using hardcoded CSV URLs only", error=str(err))

        # httpx.Client is thread-safe — parallel downloads with shared connection pool
        result = _download_all_departments(
            resources_by_dept, client=client, tmp_dir=tmp_dir, progress=progress
        )

    # -- Merge temporary parquets into final file --------------------------------------

    if result.dept_count == 0:
        shutil.rmtree(tmp_dir, ignore_errors=True)
        raise DownloadStageError("No climatologie data downloaded from any department")

    # Lazy scan all temporary parquets and write the merged result.
    # This avoids loading all departments into memory simultaneously.
    merged_path = dest_dir / _MERGED_FILENAME
    try:
        lazy_frames = pl.scan_parquet(tmp_dir / "*.parquet")
        lazy_frames.sink_parquet(merged_path)
    finally:
        shutil.rmtree(tmp_dir, ignore_errors=True)

    log_kwargs: dict[str, object] = {
        "departments_count": result.dept_count,
        "rows_count": result.total_rows,
    }
    if result.failed_count > 0:
        log_kwargs["failed_count"] = result.failed_count
        logger.warning("Climatologie merge completed with failures", **log_kwargs)
    else:
        logger.info("Climatologie merge completed", **log_kwargs)

    return merged_path
