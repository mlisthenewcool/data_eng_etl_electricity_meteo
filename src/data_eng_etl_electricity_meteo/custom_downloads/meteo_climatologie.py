"""Download Météo France climatologie data from data.gouv.fr.

Downloads hourly climatology data for all 95 metropolitan French departments.
Uses the data.gouv.fr API to discover resources and prefers Parquet files
(auto-generated by Hydra, ~20x smaller) when available, falling back to CSV.gz.
If the API is unreachable, falls back to hardcoded CSV.gz URLs.

Hydra auto-converts large CSV.gz files into Parquet
(threshold configured server-side by data.gouv.fr — see Hydra source for current value).
Parquet availability per department depends on this threshold and may change over time.

The per-department files are normalized to a uniform all-Utf8 schema and merged into a
single Parquet via lazy scanning.
"""

import re
import shutil
import sys
from concurrent.futures import ThreadPoolExecutor, as_completed
from dataclasses import dataclass
from datetime import UTC, datetime
from pathlib import Path
from typing import TypedDict

import httpx
import polars as pl
from tqdm import tqdm

from data_eng_etl_electricity_meteo.core.exceptions import DownloadStageError
from data_eng_etl_electricity_meteo.core.logger import get_logger
from data_eng_etl_electricity_meteo.core.settings import settings
from data_eng_etl_electricity_meteo.utils.progress import (
    BatchProgressFactory,
    DownloadProgressReporter,
)

logger = get_logger("dl.meteo_clim")

# data.gouv.fr API for the Météo France HOR (hourly) dataset
# Resources endpoint requires API v2 (v1 returns 405 on /resources/)
DATAGOUV_METEO_FRANCE_CLIMATOLOGIE_HOR_DATASET_ID = "6569b4473bedf2e7abad3b72"
_RESOURCES_API_URL = (
    "https://www.data.gouv.fr/api/2/datasets/"
    f"{DATAGOUV_METEO_FRANCE_CLIMATOLOGIE_HOR_DATASET_ID}/resources/"
)

# Direct download base URL (used for CSV.gz fallback when API is unavailable)
_BASE_URL = "https://object.files.data.gouv.fr/meteofrance/data/synchro_ftp/BASE/HOR"

# Metropolitan France departments: 01-19, 20 (Corse), 21-95 (95 total)
# data.gouv.fr uses "20" for Corse — "2A"/"2B" files do not exist (HTTP 404)
DEPARTMENTS: frozenset[str] = frozenset(
    {
        *{f"{i:02d}" for i in range(1, 20)},
        "20",
        *{f"{i:02d}" for i in range(21, 96)},
    }
)

# Landing output filename
_MERGED_FILENAME = "merged.parquet"

# Columns retained at download time (aligned with BRONZE_COLUMNS in
# meteo_france_climatologie.py). Pruning 196 → 16 columns here avoids
# OOM during the merge of 95 per-department parquets (~9 GB → ~760 MB).
_LANDING_COLUMNS = [
    "NUM_POSTE",
    "AAAAMMJJHH",
    "GLO",
    "INS",
    "N",
    "FF",
    "DD",
    "FXI",
    "T",
    "TX",
    "TN",
    "TD",
    "U",
    "RR1",
    "PSTAT",
    "PMER",
]

_TITLE_RE = re.compile(r"HOR_departement_(\w+)_periode_(\d{4})-(\d{4})")
_API_PAGE_SIZE = 50
_DOWNLOAD_MAX_WORKERS = 8


class DepartmentResource(TypedDict):
    """Download URLs for a single department."""

    dept: str
    csv_url: str
    parquet_url: str | None


# --------------------------------------------------------------------------------------
# Resource discovery
# --------------------------------------------------------------------------------------


def _fetch_department_resources(
    *,
    client: httpx.Client,
    year_start: int,
    year_end: int,
) -> dict[str, DepartmentResource]:
    """Discover department resources via the data.gouv.fr API.

    Queries the dataset resources endpoint, filters by period and department, and
    returns download URLs (CSV.gz + optional Parquet Hydra).

    The result may be partial (not all 95 departments). The caller is responsible for
    supplementing with fallback URLs for missing departments.

    Parameters
    ----------
    client
        HTTP client for API requests.
    year_start
        Start year of the period.
    year_end
        End year of the period.

    Returns
    -------
    dict[str, DepartmentResource]
        Mapping ``dept → resource`` for departments found via the API.
        May be empty if the API returns no matching resources.

    Raises
    ------
    httpx.HTTPError
        If the API call fails (network, timeout, HTTP status).
    """
    period = f"{year_start}-{year_end}"
    resources: dict[str, DepartmentResource] = {}
    page = 1

    while True:
        response = client.get(
            _RESOURCES_API_URL,
            params={
                "q": f"periode_{period}",
                "page_size": _API_PAGE_SIZE,
                "page": page,
            },
        )
        response.raise_for_status()
        data = response.json()

        for res in data.get("data", []):
            title = res.get("title", "")
            match = _TITLE_RE.match(title)
            if not match:
                continue

            dept = match.group(1)
            res_period = f"{match.group(2)}-{match.group(3)}"
            if dept not in DEPARTMENTS or res_period != period:
                continue

            extras = res.get("extras", {}) or {}
            resources[dept] = {
                "dept": dept,
                "csv_url": res["url"],
                "parquet_url": extras.get("analysis:parsing:parquet_url"),
            }

        total = data.get("total", 0)
        if page * _API_PAGE_SIZE >= total:
            break
        page += 1

    with_parquet = sum(1 for r in resources.values() if r["parquet_url"])
    logger.info(
        "API discovery completed",
        departments_found=len(resources),
        with_parquet=with_parquet,
        period=period,
    )
    return resources


def _build_fallback_resources(
    *,
    year_start: int,
    year_end: int,
) -> dict[str, DepartmentResource]:
    """Build CSV.gz resource dict from hardcoded URL pattern.

    Used as the base for all downloads — API discovery enriches these entries with
    Parquet URLs where available. Parquet Hydra URLs use UUIDs and cannot be guessed
    without the API, so ``parquet_url`` is always ``None`` here.

    Parameters
    ----------
    year_start
        Start year of the period.
    year_end
        End year of the period.

    Returns
    -------
    dict[str, DepartmentResource]
        Mapping ``dept → resource`` with ``parquet_url`` always None.
    """
    return {
        dept: {
            "dept": dept,
            "csv_url": f"{_BASE_URL}/H_{dept}_latest-{year_start}-{year_end}.csv.gz",
            "parquet_url": None,
        }
        for dept in sorted(DEPARTMENTS)
    }


